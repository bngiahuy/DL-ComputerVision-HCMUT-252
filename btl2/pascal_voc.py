def get_tp_matrix(pd_boxes, pd_cls, gt_boxes, gt_cls, iouv):
    """Tính toán matrix True Positive dựa trên IoU và class."""
    tp = torch.zeros(pd_boxes.shape[0], iouv.shape[0], dtype=torch.bool, device=pd_boxes.device)
    if gt_boxes.shape[0] == 0:
        return tp

    iou = box_iou(gt_boxes, pd_boxes)
    for j, step in enumerate(iouv):
        matches = (iou >= step) & (gt_cls[:, None] == pd_cls[None, :])
        if matches.any():
            for pd_idx in range(pd_boxes.shape[0]):
                gt_indices = matches[:, pd_idx].nonzero(as_tuple=False)
                if len(gt_indices) > 0:
                    best_match = gt_indices[iou[gt_indices, pd_idx].argmax()]
                    tp[pd_idx, j] = True
                    matches[best_match, :] = False
    return tp

def evaluate_map(net, val_loader, device, names, iou_thres=0.5, conf_thres=0.001):
    net.eval()
    metrics = DetMetrics(names=names)
    iouv = torch.linspace(0.5, 0.95, 10).to(device)
    stats_added = False

    for images, targets in tqdm(val_loader, desc="Evaluating"):
        images = images.to(device)
        with torch.no_grad():
            preds = net(images)
        processed_preds = non_max_suppression(preds, conf_thres, iou_thres)

        for i, p in enumerate(processed_preds):
            target = targets[i]
            gt_boxes = target['boxes'].to(device)
            gt_cls = target['labels'].to(device)

            if len(p) == 0:
                if len(gt_cls) > 0:
                    metrics.stats['tp'].append(np.zeros((0, 10), dtype=bool))
                    metrics.stats['conf'].append(np.zeros(0))
                    metrics.stats['pred_cls'].append(np.zeros(0))
                    metrics.stats['target_cls'].append(gt_cls.cpu().numpy())
                    stats_added = True
                continue

            p_boxes = p[:, :4].clone()
            orig_img_size = target['orig_size'].cpu().tolist()
            p_boxes = scale_boxes((512, 512), p_boxes, orig_img_size)

            tp = get_tp_matrix(p_boxes, p[:, 5], gt_boxes, gt_cls, iouv)

            metrics.stats['tp'].append(tp.cpu().numpy())
            metrics.stats['conf'].append(p[:, 4].cpu().numpy())
            metrics.stats['pred_cls'].append(p[:, 5].cpu().numpy())
            metrics.stats['target_cls'].append(gt_cls.cpu().numpy())
            stats_added = True

    if not stats_added:
        print("Warning: No samples were added to metrics. Check conf_thres or data loading.")
        return {}

    metrics.process()
    results = metrics.results_dict()

    map50 = results.get('metrics/mAP50(B)', 0)
    print(f"\nmAP50: {map50:.4f}")
    return results

