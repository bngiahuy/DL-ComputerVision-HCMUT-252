from types import SimpleNamespace

def prepare_yolo_targets(batch_targets, device, target_size=(512, 512)):
    yolo_targets = []
    for i, target in enumerate(batch_targets):
        boxes = target['boxes']
        labels = target['labels']
        orig_h, orig_w = target['orig_size']
        if len(boxes) == 0: continue
        bw = (boxes[:, 2] - boxes[:, 0]) / orig_w
        bh = (boxes[:, 3] - boxes[:, 1]) / orig_h
        cx = (boxes[:, 0] + boxes[:, 2]) / (2 * orig_w)
        cy = (boxes[:, 1] + boxes[:, 3]) / (2 * orig_h)
        batch_idx = torch.full((len(labels), 1), i)
        target_tensor = torch.cat([batch_idx, labels.unsqueeze(1).float(), cx.unsqueeze(1), cy.unsqueeze(1), bw.unsqueeze(1), bh.unsqueeze(1)], dim=1)
        yolo_targets.append(target_tensor)
    return torch.cat(yolo_targets, dim=0).to(device) if yolo_targets else torch.zeros((0, 6)).to(device)

